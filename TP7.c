static void MAX30101_Write_Data(void)
{
	ret = HAL_I2C_Master_Transmit(&hi2c1, Adress_8b, buf_I2C_TX, 2, HAL_MAX_DELAY);

	if(ret != HAL_OK)
	{
		sprintf((char*)buf_UART,"Error Tx\r\n");
	}
	else
	{
		sprintf((char*)buf_UART,message);

	}
	HAL_UART_Transmit(&huart3, buf_UART, strlen((char*)buf_UART), HAL_MAX_DELAY);
}

static void MAX30101_Read_Data(void)
{
	ret = HAL_I2C_Master_Transmit(&hi2c1, Adress_8b,buf_I2C_TX, 2, HAL_MAX_DELAY);
	if(ret != HAL_OK)
	{
		sprintf((char*)buf_UART,"Error Tx\r\n");
	}
	else
	{
		ret = HAL_I2C_Master_Receive(&hi2c1, Adress_8b, buf_I2C_RX, 1, HAL_MAX_DELAY);
		if(ret != HAL_OK)
			{
				sprintf((char*)buf_UART,"Error Tx\r\n");
			}
		else
		{
			sprintf((char*)buf_UART,message,buf_I2C_RX[0]);
		}
	}
	HAL_UART_Transmit(&huart3, buf_UART, strlen((char*)buf_UART), HAL_MAX_DELAY);
}

